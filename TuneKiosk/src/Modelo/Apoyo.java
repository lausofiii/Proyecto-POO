package Modelo;

/**
 *
 * @author laura
 */
public class Apoyo {
    
    String nombre;
    double costo;
    String unidad;
    

    public Apoyo() {};
   
    public Apoyo(String nombre, double costo, String unidad) {
        this.nombre = nombre;
        this.costo = costo;
        this.unidad = unidad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getCosto() {
        return costo;
    }

    public void setCosto(double costo) {
        this.costo = costo;
    }

    public String getUnidad() {
        return unidad;
    }

    public void setUnidad(String unidad) {
        this.unidad = unidad;
    }
    
    
    
    
}
