    public HerramientasIngeniero(ListaMateriales mati, ListaManoDeObra mo, ListaHerramientas mh) {
        if(mati.vertamanomat()==0){
            mati.agregarMaterial();
        }
        if(mo.vertamanomob()==0){
            mo.agregarManoDeObra();
        }
        if(mh.vertamanoher()==0){
            mh.agregarHerramienta();
        }
        mat=mati;
        mob=mo;
        her=mh;
        initComponents();
        lista = her.getHerramientas();
        control.llenarTablaManoDeObra(tablaHerramientas, lista);
        control.iniciaVentana(this);
    }
    

