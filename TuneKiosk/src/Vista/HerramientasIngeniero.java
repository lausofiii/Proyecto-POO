    private void tablaHerramientasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaHerramientasMouseClicked
      int row = tablaHerramientas.rowAtPoint(evt.getPoint());
        if(row>=0){
            String herramienta = (String)tablaHerramientas.getValueAt(row, 0);
            double costo = Double.parseDouble(tablaHerramientas.getValueAt(row, 1).toString());
            String unidad = (String)tablaHerramientas.getValueAt(row, 2);
            String mensaje = "Herramienta: "+herramienta+" \n"+
                             "Costo: "+costo+"$"+" \n"+
                             "Cobra por: "+unidad;
            String[] opciones = {"Volver","Eliminar herramienta"};
            int opc = JOptionPane.showOptionDialog(null, mensaje, "Detalles de la Herramienta", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opciones, opciones[1]);
            switch(opc) {
                case 1: her.setHerramientas(lista);
                        Iterator<Apoyo> it = lista.iterator();
                        Apoyo eliminar = new Apoyo();
                        while (it.hasNext()) {
                            Apoyo herr = it.next();
                            if (herr.getNombre() == herramienta) {
                                eliminar = herr;
                            }
                        }
                        her.eliminarHerramienta(eliminar);
                        lista = her.getHerramientas();
                        HerramientasIngeniero ventana = new HerramientasIngeniero(mat,mob,her);
                        control.activaVentana(ventana, this);
                        break;
            }
        }
    }//GEN-LAST:event_tablaHerramientasMouseClicked

